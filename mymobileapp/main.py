        db = {'all': {'单选题': [], '多选题': [], '填空题': [], '判断题': []}}
        
        # 简化后的数据解析逻辑，即使读取失败也不报错
        def parse_data():
            lines = [l.strip() for l in RAW_DATA.splitlines() if l.strip()]
            current_q = None
            for line in lines:
                if any(t in line for t in ["单选题", "多选题", "填空题", "判断题"]):
                    # 简单的正则匹配或关键字查找
                    q_type = "单选题" if "单选题" in line else "多选题" if "多选题" in line else "填空题" if "填空题" in line else "判断题"
                    current_q = {'question': line, 'options': [], 'answer': '', 'type': q_type}
                    db['all'][q_type].append(current_q)
